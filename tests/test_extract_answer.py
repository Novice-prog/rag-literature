"""Юнит-тесты разбора итогового ответа LLM."""

import pytest

from main import extract_answer


@pytest.mark.parametrize(
    "text, expected",
    [
        ("[ОТВЕТ: A]", 1),
        ("Итоговый ответ: [ОТВЕТ: B]", 2),
        ("...anyway [ОТВЕТ: C]", 3),
        ("Ответ: D", 4),
        # Кириллические буквы вариантов
        ("Ответ: а", 1),
        ("Ответ: в", 3),
        ("Ответ: г", 4),
        # Ложное совпадение «варианты ответа A» не должно перебивать финальный ответ
        ("Рассмотрим варианты ответа A, B, C, D. Итоговый ответ: [ОТВЕТ: C]", 3),
        ("вариант ответа a кажется верным, но нет. Ответ: D", 4),
        # Берётся последняя метка
        ("Ответ: A ... на самом деле [ОТВЕТ: B]", 2),
        # Нет распознаваемой метки
        ("нет метки, просто текст про ответа", 0),
        ("", 0),
    ],
)
def test_extract_answer(text, expected):
    assert extract_answer(text) == expected
